class animal():
    def __init__(self):
        pass
    def sound(self):
        print("Animal makes a sound")

class dog(animal):
    def __init__self():
        pass
    def sound(self):
        print("Dog Barks")  #Method Overriding

class bird(animal):
    def __init__self():
        pass
    def sound(self):
        print("birds sing")

